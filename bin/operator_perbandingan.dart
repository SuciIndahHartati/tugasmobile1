void main () {
  int data1 = 15;
  int data2 = 5;
  print("$data1 == $data2 | ${data1 == data2}");
  print("$data1 != $data2 | ${data1 != data2}");
  print("$data1 > $data2 | ${data1 > data2}");
  print("$data1 < $data2 | ${data1 < data2}");
  print("$data1 <= $data2 | ${data1 <= data2}");
  print("$data1 >= $data2 | ${data1 >= data2}");
}